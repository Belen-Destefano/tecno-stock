export const getData = (categoryId)=>{
    
    return new Promise ((resolve, reject) => {

        const productsFilter = products.filter(product => product.category === categoryId)

        setTimeout(()=>{
           
            categoryId? resolve(productsFilter): resolve(products)
        
            reject ('Ha ocurrido un error')
            
        }, 1000)
    })
}

export const getProduct = (productId)=>{
    
    return new Promise ((resolve, reject) => {

        // puse los id como strings en los objetos, por que el useParams devuelve strings. sino tenia que ponerme number (productId abajo como otra opcion)
        const productsFind = products.find(product => product.id === productId)

        setTimeout(()=>{
           
            productId? resolve(productsFind): resolve(products)
        
            reject ('Ha ocurrido un error')
            
        }, 1000)
    })
}